package com.munaf.A31_TASK_SCHEDULING.schedule;

import org.springframework.scheduling.annotation.Async;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.stereotype.Component;

@Component
public class SchedulingAnnotation {


    @Scheduled(fixedDelay = 1000)
    @Async
    public void executeTask() throws InterruptedException {
        System.out.println("Task1 Started with thread name : " + Thread.currentThread().getName());

        Thread.sleep(10000);
    }


    @Scheduled(fixedDelay = 1000)
    public void executeTask2() throws InterruptedException {
        System.out.println("Task2 Started with thread name : " + Thread.currentThread().getName());

        Thread.sleep(3000);
    }

}
